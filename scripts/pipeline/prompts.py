SUMMARY_PROMPT = """\
以下は{house}{meeting}の「{topic}」に関する発言です。
各発言について、要約・分類・キーワード抽出を行ってください。

## 要約の文体ルール（最重要）
要約は発言内容そのものを簡潔に表現する。報告文体は絶対に使わない。

❌ NG（報告文体）:
「AI定義が曖昧すぎると指摘し、EUとの比較を求めた。」
「影響試算を提出すると約束した。」

✅ OK（発言内容そのもの）:
「AI定義が曖昧すぎて、レコメンドエンジンまで規制対象になりうる。EUとの比較と試算を出せ。」
「採決前に影響試算を必ず提出する。国会報告義務の明記も検討する。」

## レベル別指示
- easy（やさしく）: 難しい言葉を言い換え、断定調（〜だ、〜する）、60〜80字
- teen（標準）: 専門用語には括弧で短い補足、体言止めOK、80〜100字
- adult（詳しく）: 政治・法律用語そのまま、論点を端的に、体言止めOK、60〜80字

## tension分類
- 追及: 野党が政府・与党を問いただす質問
- 答弁: 大臣・政府委員の回答
- 再追及: 答弁が不十分として重ねて追及
- 確認: 事実関係の確認、穏やかな質疑
- 割込み: 議事進行発言、野次、委員長の制止など

## 発言一覧
{speeches}

## コミットメント抽出
答弁の中で大臣・政府委員が具体的に約束・表明したことを抽出する。
例: 「採決前に影響試算を提出する」「次の国会で法案を出す」「検討会を設置する」
曖昧な「検討する」「努力する」は含めない。具体的なアクションのみ。

## 出力（JSONのみ、他のテキストは不要）
```json
{{
  "speeches": [
    {{
      "speechOrder": 4,
      "tension": "追及",
      "keywords": ["キーワード1", "キーワード2", "キーワード3"],
      "quote": "原文から最も重要な一文をそのまま抜粋（改変不可）",
      "summaries": {{
        "easy": "...(60-80字)",
        "teen": "...(80-100字)",
        "adult": "...(60-80字)"
      }}
    }}
  ],
  "commitments": ["具体的な約束1", "具体的な約束2"]
}}
```"""


